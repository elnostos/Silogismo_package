# Primera Figura
absurdo("AB","CA","AAA") # BARBARA
absurdo("AB","CA","AII") # DARII
absurdo("AB","CA","EAE") # CELARENT
absurdo("AB","CA","EIO") # FERIO
absurdo("AB", "CA", "AAI") # --> BARBARIP

# Segunda figura:
absurdo("BA","CA","AEE") # CAMESTRES
absurdo("BA","CA","EIO") # FESTINO
absurdo("BA","CA","AOO") # BAROCO
absurdo("BA","CA","EAE") # CESARE
absurdo("BA","CA","AEO") # R. A: AAA (SI)
absurdo("BA","CA","EAO") # R. A: EAE (SI)


# Tercera Figura:
absurdo("AB","AC","AAI") # DARAPTI
